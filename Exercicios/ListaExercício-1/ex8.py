# Fazer um algoritmo que leia quatro números e imprime a média aritmética dos quatro números.

nun1 = float(input("Digite o valor do primeiro número: "))
nun2 = float(input("Digite o valor do segundo número: "))
nun3 = float(input("Digite o valor do terceiro número: "))
nun4 = float(input("Digite o valor do quarto núemro: "))

